-- =========================================================
-- 14. FUNCTION: REGISTER WHATSAPP MESSAGE
-- Useful for OpenClaw webhook logging.
-- =========================================================

CREATE OR REPLACE FUNCTION api.registrar_whatsapp_mensaje(
    p_whatsapp_number text,
    p_direction text,
    p_message_type text DEFAULT 'text',
    p_message_text text DEFAULT NULL,
    p_latitude numeric DEFAULT NULL,
    p_longitude numeric DEFAULT NULL,
    p_raw_payload jsonb DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = api, public
AS $$
DECLARE
    v_direction text;
    v_message_id integer;
BEGIN
    v_direction := upper(trim(p_direction));

    IF v_direction NOT IN ('INBOUND', 'OUTBOUND') THEN
        RAISE EXCEPTION 'Invalid direction: %. Use INBOUND or OUTBOUND.', p_direction;
    END IF;

    INSERT INTO api.whatsapp_mensajes (
        whatsapp_number,
        direction,
        message_type,
        message_text,
        latitude,
        longitude,
        raw_payload
    )
    VALUES (
        trim(p_whatsapp_number),
        v_direction,
        p_message_type,
        p_message_text,
        p_latitude,
        p_longitude,
        p_raw_payload
    )
    RETURNING id INTO v_message_id;

    INSERT INTO api.whatsapp_conversaciones (
        whatsapp_number,
        estado,
        last_inbound_text,
        last_outbound_text,
        last_message_at
    )
    VALUES (
        trim(p_whatsapp_number),
        CASE
            WHEN v_direction = 'INBOUND' THEN 'ASKING_NAME_AND_ITEMS'
            ELSE 'NEW'
        END,
        CASE WHEN v_direction = 'INBOUND' THEN p_message_text ELSE NULL END,
        CASE WHEN v_direction = 'OUTBOUND' THEN p_message_text ELSE NULL END,
        now()
    )
    ON CONFLICT (whatsapp_number)
    DO UPDATE SET
        last_inbound_text =
            CASE
                WHEN v_direction = 'INBOUND' THEN p_message_text
                ELSE api.whatsapp_conversaciones.last_inbound_text
            END,
        last_outbound_text =
            CASE
                WHEN v_direction = 'OUTBOUND' THEN p_message_text
                ELSE api.whatsapp_conversaciones.last_outbound_text
            END,
        last_message_at = now(),
        updated_at = now();

    RETURN jsonb_build_object(
        'ok', true,
        'message_id', v_message_id
    );
END;
$$;


-- =========================================================
-- 15. FUNCTION: CONFIRM WHATSAPP ORDER
--
-- This is the main PostgREST RPC endpoint:
-- POST /rpc/confirmar_pedido_whatsapp
-- =========================================================

CREATE OR REPLACE FUNCTION api.confirmar_pedido_whatsapp(
    p_whatsapp_number text,
    p_customer_name text,
    p_payment_method text,
    p_latitude numeric,
    p_longitude numeric,
    p_detected_address text,
    p_confirmed_address text,
    p_items jsonb,
    p_base_url text DEFAULT 'http://localhost:3000',
    p_delivery numeric DEFAULT 0,
    p_observacion text DEFAULT NULL
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = api, public
AS $$
DECLARE
    v_cliente_id integer;
    v_pedido_id integer;
    v_pedido_num text;

    v_payment_method text;
    v_item jsonb;
    v_index integer := 0;

    v_producto_id integer;
    v_producto_texto text;
    v_cantidad numeric(14,3);
    v_unidad text;
    v_precio_unitario numeric(14,2);
    v_total_linea numeric(14,2);

    v_subtotal numeric(14,2) := 0;
    v_delivery numeric(14,2) := COALESCE(p_delivery, 0);
    v_total numeric(14,2);

    v_order_url text;
    v_items_url text;
    v_maps_url text;

    v_email_subject text;
    v_email_body text;
    v_items_text text := '';

    v_email_log_id integer;
BEGIN
    -- -----------------------------------------------------
    -- Basic validations
    -- -----------------------------------------------------

    IF p_whatsapp_number IS NULL OR trim(p_whatsapp_number) = '' THEN
        RAISE EXCEPTION 'whatsapp_number is required';
    END IF;

    IF p_customer_name IS NULL OR trim(p_customer_name) = '' THEN
        RAISE EXCEPTION 'customer_name is required';
    END IF;

    IF p_items IS NULL OR jsonb_typeof(p_items) <> 'array' OR jsonb_array_length(p_items) = 0 THEN
        RAISE EXCEPTION 'items must be a non-empty JSON array';
    END IF;

    IF v_delivery < 0 THEN
        RAISE EXCEPTION 'delivery cannot be negative';
    END IF;

    v_payment_method := upper(replace(trim(p_payment_method), ' ', '_'));

    IF v_payment_method NOT IN ('YAPE', 'PLIN', 'TRANSFERENCIA', 'CONTRA_ENTREGA') THEN
        RAISE EXCEPTION 'Invalid payment_method: %. Use YAPE, PLIN, TRANSFERENCIA, or CONTRA_ENTREGA.', p_payment_method;
    END IF;


    -- -----------------------------------------------------
    -- Upsert customer
    -- -----------------------------------------------------

    INSERT INTO api.clientes_whatsapp (
        whatsapp_number,
        nombre,
        last_order_at
    )
    VALUES (
        trim(p_whatsapp_number),
        trim(p_customer_name),
        now()
    )
    ON CONFLICT (whatsapp_number)
    DO UPDATE SET
        nombre = EXCLUDED.nombre,
        last_order_at = now(),
        updated_at = now()
    RETURNING id INTO v_cliente_id;


    -- -----------------------------------------------------
    -- Create order header
    -- -----------------------------------------------------

    v_pedido_num := 'PED-' || lpad(nextval('api.pedido_num_seq')::text, 6, '0');

    INSERT INTO api.pedidos (
        pedido_num,
        cliente_id,
        canal,
        estado,
        metodo_pago,
        moneda,
        subtotal,
        delivery,
        total,
        observacion
    )
    VALUES (
        v_pedido_num,
        v_cliente_id,
        'WHATSAPP',
        'CONFIRMADO',
        v_payment_method,
        'PEN',
        0,
        v_delivery,
        0,
        p_observacion
    )
    RETURNING id INTO v_pedido_id;


    -- -----------------------------------------------------
    -- Insert items
    -- -----------------------------------------------------

    FOR v_item IN
        SELECT value
        FROM jsonb_array_elements(p_items)
    LOOP
        v_index := v_index + 1;

        v_producto_id := NULL;
        v_producto_texto := NULL;
        v_cantidad := NULL;
        v_unidad := NULL;
        v_precio_unitario := NULL;
        v_total_linea := NULL;

        -- Product text
        v_producto_texto := COALESCE(
            NULLIF(trim(v_item ->> 'producto_texto'), ''),
            NULLIF(trim(v_item ->> 'product'), ''),
            NULLIF(trim(v_item ->> 'nombre'), '')
        );

        IF v_producto_texto IS NULL THEN
            RAISE EXCEPTION 'Item % is missing producto_texto/product/nombre', v_index;
        END IF;

        -- Quantity
        v_cantidad := NULLIF(trim(v_item ->> 'cantidad'), '')::numeric;

        IF v_cantidad IS NULL OR v_cantidad <= 0 THEN
            RAISE EXCEPTION 'Item % has invalid cantidad', v_index;
        END IF;

        -- Unit
        v_unidad := upper(NULLIF(trim(COALESCE(v_item ->> 'unidad', v_item ->> 'unit')), ''));

        -- Product id from JSON if present
        IF NULLIF(trim(v_item ->> 'producto_id'), '') IS NOT NULL THEN
            v_producto_id := (v_item ->> 'producto_id')::integer;
        END IF;

        -- Product lookup by text if producto_id was not provided
        IF v_producto_id IS NULL THEN
            SELECT p.id
            INTO v_producto_id
            FROM api.productos p
            WHERE
                upper(p.cdg_prod) = upper(v_producto_texto)
                OR upper(p.nombre) = upper(v_producto_texto)
            ORDER BY p.id
            LIMIT 1;
        END IF;

        -- Price from JSON if present
        IF NULLIF(trim(v_item ->> 'precio_unitario'), '') IS NOT NULL THEN
            v_precio_unitario := (v_item ->> 'precio_unitario')::numeric;
        END IF;

        -- Price lookup if not passed
        IF v_precio_unitario IS NULL AND v_producto_id IS NOT NULL THEN
            SELECT pp.precio
            INTO v_precio_unitario
            FROM api.producto_precios pp
            WHERE pp.producto_id = v_producto_id
              AND pp.active = true
              AND pp.moneda = 'PEN'
              AND (v_unidad IS NULL OR upper(pp.unidad) = upper(v_unidad))
              AND pp.valid_from <= current_date
              AND (pp.valid_to IS NULL OR pp.valid_to >= current_date)
            ORDER BY
                CASE
                    WHEN v_unidad IS NOT NULL AND upper(pp.unidad) = upper(v_unidad) THEN 0
                    ELSE 1
                END,
                pp.valid_from DESC,
                pp.id DESC
            LIMIT 1;
        END IF;

        v_precio_unitario := COALESCE(v_precio_unitario, 0);

        IF v_precio_unitario < 0 THEN
            RAISE EXCEPTION 'Item % has invalid precio_unitario', v_index;
        END IF;

        v_total_linea := round((v_cantidad * v_precio_unitario)::numeric, 2);

        INSERT INTO api.pedido_items (
            pedido_id,
            producto_id,
            producto_texto,
            cantidad,
            unidad,
            precio_unitario,
            total_linea,
            raw_item
        )
        VALUES (
            v_pedido_id,
            v_producto_id,
            v_producto_texto,
            v_cantidad,
            v_unidad,
            v_precio_unitario,
            v_total_linea,
            v_item
        );

        v_subtotal := v_subtotal + v_total_linea;

        v_items_text := v_items_text ||
            v_index::text || '. ' ||
            v_producto_texto || ' x ' ||
            v_cantidad::text || ' ' ||
            COALESCE(v_unidad, '') ||
            ' — S/ ' || to_char(v_total_linea, 'FM999999990.00') ||
            E'\n';
    END LOOP;


    -- -----------------------------------------------------
    -- Address
    -- -----------------------------------------------------

    INSERT INTO api.pedido_direcciones (
        pedido_id,
        latitud,
        longitud,
        direccion_detectada,
        direccion_confirmada,
        confirmed
    )
    VALUES (
        v_pedido_id,
        p_latitude,
        p_longitude,
        p_detected_address,
        p_confirmed_address,
        true
    );


    -- -----------------------------------------------------
    -- URLs
    -- -----------------------------------------------------

    v_total := v_subtotal + v_delivery;

    v_order_url := rtrim(p_base_url, '/') ||
        '/v_pedidos_logistica?id=eq.' ||
        v_pedido_id::text;

    v_items_url := rtrim(p_base_url, '/') ||
        '/v_pedido_items_logistica?pedido_id=eq.' ||
        v_pedido_id::text;

    IF p_latitude IS NOT NULL AND p_longitude IS NOT NULL THEN
        v_maps_url := 'https://www.google.com/maps?q=' ||
            p_latitude::text ||
            ',' ||
            p_longitude::text;
    END IF;


    -- -----------------------------------------------------
    -- Update order totals and URLs
    -- -----------------------------------------------------

    UPDATE api.pedidos
    SET
        subtotal = v_subtotal,
        delivery = v_delivery,
        total = v_total,
        order_url = v_order_url,
        items_url = v_items_url,
        maps_url = v_maps_url,
        updated_at = now()
    WHERE id = v_pedido_id;

    UPDATE api.clientes_whatsapp
    SET
        last_order_at = now(),
        last_order_id = v_pedido_id,
        last_order_num = v_pedido_num,
        last_order_total = v_total,
        last_payment_method = v_payment_method,
        last_latitude = p_latitude,
        last_longitude = p_longitude,
        last_maps_url = v_maps_url,
        last_written_address = CASE
            WHEN p_confirmed_address IS NOT NULL AND position(E'\nReferencia del mapa:' in p_confirmed_address) > 0
                THEN split_part(p_confirmed_address, E'\nReferencia del mapa:', 1)
            ELSE p_confirmed_address
        END,
        last_detected_address = p_detected_address,
        last_confirmed_address = p_confirmed_address,
        last_order_snapshot = jsonb_build_object(
            'pedido_id', v_pedido_id,
            'pedido_num', v_pedido_num,
            'total', v_total,
            'payment_method', v_payment_method,
            'items', p_items,
            'order_url', v_order_url,
            'maps_url', v_maps_url
        ),
        updated_at = now()
    WHERE id = v_cliente_id;


    -- -----------------------------------------------------
    -- Update conversation state
    -- -----------------------------------------------------

    INSERT INTO api.whatsapp_conversaciones (
        whatsapp_number,
        cliente_id,
        estado,
        pedido_id,
        pedido_borrador,
        last_message_at
    )
    VALUES (
        trim(p_whatsapp_number),
        v_cliente_id,
        'CONFIRMED',
        v_pedido_id,
        jsonb_build_object(
            'pedido_id', v_pedido_id,
            'pedido_num', v_pedido_num,
            'total', v_total,
            'items', p_items
        ),
        now()
    )
    ON CONFLICT (whatsapp_number)
    DO UPDATE SET
        cliente_id = EXCLUDED.cliente_id,
        estado = 'CONFIRMED',
        pedido_id = EXCLUDED.pedido_id,
        pedido_borrador = EXCLUDED.pedido_borrador,
        last_message_at = now(),
        updated_at = now();


    -- -----------------------------------------------------
    -- Create logistics email outbox row
    -- -----------------------------------------------------

    v_email_subject := 'Nuevo pedido WhatsApp ' || v_pedido_num;

    v_email_body :=
        'Nuevo pedido confirmado por WhatsApp.' || E'\n\n' ||
        'Pedido: ' || v_pedido_num || E'\n' ||
        'Cliente: ' || trim(p_customer_name) || E'\n' ||
        'WhatsApp: ' || trim(p_whatsapp_number) || E'\n' ||
        'Pago: ' || v_payment_method || E'\n' ||
        'Subtotal: S/ ' || to_char(v_subtotal, 'FM999999990.00') || E'\n' ||
        'Delivery: S/ ' || to_char(v_delivery, 'FM999999990.00') || E'\n' ||
        'Total: S/ ' || to_char(v_total, 'FM999999990.00') || E'\n\n' ||
        'Items:' || E'\n' ||
        v_items_text || E'\n' ||
        'Dirección confirmada:' || E'\n' ||
        COALESCE(p_confirmed_address, p_detected_address, '(sin dirección)') || E'\n\n' ||
        'Pedido:' || E'\n' ||
        v_order_url || E'\n\n' ||
        'Items:' || E'\n' ||
        v_items_url || E'\n\n' ||
        'Ubicación:' || E'\n' ||
        COALESCE(v_maps_url, '(sin ubicación)') || E'\n';

    INSERT INTO api.email_logistica_log (
        pedido_id,
        recipient,
        subject,
        body,
        status
    )
    VALUES (
        v_pedido_id,
        'logistica@replau.com',
        v_email_subject,
        v_email_body,
        'PENDING'
    )
    RETURNING id INTO v_email_log_id;


    -- -----------------------------------------------------
    -- Return response to OpenClaw / PostgREST
    -- -----------------------------------------------------

    RETURN jsonb_build_object(
        'ok', true,
        'pedido_id', v_pedido_id,
        'pedido_num', v_pedido_num,
        'cliente_id', v_cliente_id,
        'subtotal', v_subtotal,
        'delivery', v_delivery,
        'total', v_total,
        'payment_method', v_payment_method,
        'order_url', v_order_url,
        'items_url', v_items_url,
        'maps_url', v_maps_url,
        'email_log_id', v_email_log_id,
        'email_to', 'logistica@replau.com',
        'email_status', 'PENDING',
        'whatsapp_confirmation_text',
            'Pedido confirmado ✅' || E'\n\n' ||
            'Pedido: ' || v_pedido_num || E'\n' ||
            'Cliente: ' || trim(p_customer_name) || E'\n' ||
            'Total: S/ ' || to_char(v_total, 'FM999999990.00') || E'\n' ||
            'Pago: ' || v_payment_method || E'\n\n' ||
            'Dirección:' || E'\n' ||
            COALESCE(p_confirmed_address, p_detected_address, '(sin dirección)') || E'\n\n' ||
            'Puedes ver tu pedido aquí:' || E'\n' ||
            v_order_url
    );
END;
$$;


