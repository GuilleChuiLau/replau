-- =========================================================
-- 5. ORDER HEADER
-- =========================================================

CREATE TABLE api.pedidos (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    pedido_num text NOT NULL UNIQUE,
    cliente_id integer NOT NULL REFERENCES api.clientes_whatsapp(id) ON DELETE RESTRICT,

    canal text NOT NULL DEFAULT 'WHATSAPP',
    estado text NOT NULL DEFAULT 'CONFIRMADO',

    metodo_pago text NOT NULL,
    moneda char(3) NOT NULL DEFAULT 'PEN',

    subtotal numeric(14,2) NOT NULL DEFAULT 0,
    delivery numeric(14,2) NOT NULL DEFAULT 0,
    total numeric(14,2) NOT NULL DEFAULT 0,

    order_url text,
    items_url text,
    maps_url text,

    observacion text,

    created_at timestamptz NOT NULL DEFAULT now(),
    updated_at timestamptz NOT NULL DEFAULT now(),

    CONSTRAINT pedidos_canal_check
    CHECK (canal IN ('WHATSAPP', 'WEB', 'APP', 'MANUAL')),

    CONSTRAINT pedidos_estado_check
    CHECK (estado IN (
        'BORRADOR',
        'PENDIENTE',
        'CONFIRMADO',
        'EN_PREPARACION',
        'DESPACHADO',
        'ENTREGADO',
        'ANULADO'
    )),

    CONSTRAINT pedidos_pago_check
    CHECK (metodo_pago IN (
        'YAPE',
        'PLIN',
        'TRANSFERENCIA',
        'CONTRA_ENTREGA'
    )),

    CONSTRAINT pedidos_moneda_check
    CHECK (moneda IN ('PEN', 'USD', 'EUR')),

    CONSTRAINT pedidos_totales_check
    CHECK (
        subtotal >= 0
        AND delivery >= 0
        AND total >= 0
    )
);

CREATE TRIGGER set_updated_at_pedidos
BEFORE UPDATE ON api.pedidos
FOR EACH ROW
EXECUTE FUNCTION api.set_updated_at();


