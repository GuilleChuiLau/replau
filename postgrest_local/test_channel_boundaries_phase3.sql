DO $$
DECLARE
    v_count integer;
    v_legacy jsonb;
    v_a jsonb;
    v_b jsonb;
BEGIN
    SELECT count(*) INTO v_count FROM api.whatsapp_conversation_states WHERE customer_address='51999999999';
    IF v_count <> 3 THEN RAISE EXCEPTION 'expected 3 isolated states, got %', v_count; END IF;
    SELECT pedido_borrador INTO v_legacy FROM api.whatsapp_conversation_states WHERE channel_id='replau-main';
    SELECT pedido_borrador INTO v_a FROM api.whatsapp_conversation_states WHERE channel_id='whatsapp-account:a';
    SELECT pedido_borrador INTO v_b FROM api.whatsapp_conversation_states WHERE channel_id='whatsapp-account:b';
    IF v_legacy <> '{"item":"legacy"}'::jsonb THEN RAISE EXCEPTION 'legacy migration changed draft'; END IF;
    IF v_a <> '{"account":"A"}'::jsonb OR v_b <> '{"account":"B"}'::jsonb THEN
        RAISE EXCEPTION 'account drafts collided';
    END IF;
END $$;
