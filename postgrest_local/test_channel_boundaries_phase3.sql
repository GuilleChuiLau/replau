\set ON_ERROR_STOP on

CREATE SCHEMA api;
CREATE TABLE api.clientes_whatsapp (
    id integer PRIMARY KEY, whatsapp_number text, channel_kind text DEFAULT 'whatsapp',
    channel_id text DEFAULT 'replau-main', account_id text, customer_address text
);
CREATE TABLE api.pedidos (id integer PRIMARY KEY);
CREATE TABLE api.whatsapp_conversaciones (
    id integer PRIMARY KEY, whatsapp_number text, cliente_id integer, estado text,
    pedido_id integer, pedido_borrador jsonb, last_inbound_text text,
    last_outbound_text text, last_message_at timestamptz, created_at timestamptz,
    updated_at timestamptz, channel_kind text, channel_id text, account_id text,
    customer_address text
);
CREATE TABLE api.whatsapp_mensajes (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, whatsapp_number text,
    channel_kind text, channel_id text, account_id text, customer_address text,
    direction text, message_type text, message_text text, latitude numeric,
    longitude numeric, raw_payload jsonb, created_at timestamptz DEFAULT now()
);

