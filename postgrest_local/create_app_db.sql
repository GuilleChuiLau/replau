    p.controla_vencimiento,
    p.active,
    p.created_at,
    p.updated_at
FROM api.productos p;


CREATE OR REPLACE VIEW api.v_ordenes_compra_resumen AS
SELECT
    oc.id,
    oc.num_ocom,
    oc.fecha_ocom,
    oc.fecha_entrega,
    oc.moneda,
    oc.estado,
    pr.nombre AS proveedor_nombre,
    pr.ruc_sunat,
    pr.ruc_prv_internal,
    COUNT(d.id) AS total_lineas,
    COALESCE(SUM(d.cantidad_pedida), 0) AS total_cantidad_pedida,
    COALESCE(SUM(d.cantidad_recibida), 0) AS total_cantidad_recibida,
    COALESCE(SUM(d.cantidad_pedida * d.precio_unitario), 0) AS subtotal,
    oc.observacion,
    oc.created_at,
    oc.updated_at
FROM api.ordenes_compra oc
LEFT JOIN api.proveedores pr ON pr.id = oc.proveedor_id
LEFT JOIN api.ordenes_compra_detalle d ON d.orden_id = oc.id
GROUP BY
    oc.id,
    pr.nombre,
    pr.ruc_sunat,
    pr.ruc_prv_internal;


CREATE OR REPLACE VIEW api.v_ordenes_compra_detalle AS
SELECT
    d.id,
    d.orden_id,
    oc.num_ocom,
    oc.estado AS estado_oc,
    d.linea,
    d.producto_id,
    p.cdg_prod,
    p.nombre AS producto_nombre,
    p.tipo_producto,
    d.descripcion,
    d.cantidad_pedida,
    d.cantidad_recibida,
    d.cantidad_pedida - d.cantidad_recibida AS cantidad_pendiente,
    d.precio_unitario,
    d.impuesto,
    d.descuento,
    d.estado AS estado_linea,
    d.created_at,
    d.updated_at
FROM api.ordenes_compra_detalle d
JOIN api.ordenes_compra oc ON oc.id = d.orden_id
JOIN api.productos p ON p.id = d.producto_id;


CREATE OR REPLACE VIEW api.v_recepciones_detalle AS
SELECT
    rd.id,
    rd.recepcion_id,
    r.num_recepcion,
    r.fecha_recepcion,
    r.estado AS estado_recepcion,
    oc.num_ocom,
    rd.oc_detalle_id,
    rd.producto_id,
    p.cdg_prod,
    p.nombre AS producto_nombre,
    rd.almacen_id,
    a.codigo AS almacen_codigo,
    a.nombre AS almacen_nombre,
    rd.lote_id,
    l.lote_codigo,
    l.batch,
    l.fecha_venc,
    rd.cantidad_recibida,
    rd.pack,
    rd.envase,
    u.full_name AS operador_nombre,
    rd.observacion,
    rd.created_at
FROM api.recepcion_detalle rd
JOIN api.recepciones r ON r.id = rd.recepcion_id
LEFT JOIN api.ordenes_compra oc ON oc.id = r.orden_id
JOIN api.productos p ON p.id = rd.producto_id
JOIN api.almacenes a ON a.id = rd.almacen_id
LEFT JOIN api.lotes l ON l.id = rd.lote_id
LEFT JOIN api.app_users u ON u.id = r.operador_id;


CREATE OR REPLACE VIEW api.v_kardex AS
SELECT
    ms.id,
    ms.fecha_movimiento,
    ms.movimiento_tipo,
    CASE
        WHEN ms.movimiento_tipo IN ('RECEPCION', 'INGRESO', 'AJUSTE_POSITIVO') THEN 'ENTRADA'
        WHEN ms.movimiento_tipo IN ('SALIDA', 'AJUSTE_NEGATIVO') THEN 'SALIDA'
    END AS entrada_salida,

    COALESCE(ms.almacen_destino_id, ms.almacen_origen_id) AS almacen_id,
    a.codigo AS almacen_codigo,
    a.nombre AS almacen_nombre,

    ms.producto_id,
    p.cdg_prod,
    p.nombre AS producto_nombre,
    p.tipo_producto,

    ms.lote_id,
    l.lote_codigo,
    l.batch,
    l.fecha_venc,

    ms.cantidad,

    CASE
        WHEN ms.movimiento_tipo IN ('RECEPCION', 'INGRESO', 'AJUSTE_POSITIVO') THEN ms.cantidad
        WHEN ms.movimiento_tipo IN ('SALIDA', 'AJUSTE_NEGATIVO') THEN ms.cantidad * -1
    END AS cantidad_signo,

    ms.doc_tipo,
    ms.doc_id,
    ms.doc_linea_id,
    ms.referencia,
    u.full_name AS operador_nombre,
    ms.observacion,
    ms.created_at
FROM api.movimientos_stock ms
JOIN api.productos p ON p.id = ms.producto_id
LEFT JOIN api.lotes l ON l.id = ms.lote_id
LEFT JOIN api.almacenes a ON a.id = COALESCE(ms.almacen_destino_id, ms.almacen_origen_id)
LEFT JOIN api.app_users u ON u.id = ms.operador_id;


CREATE OR REPLACE VIEW api.v_stock_actual AS
SELECT
    almacen_id,
    almacen_codigo,
    almacen_nombre,
    producto_id,
    cdg_prod,
    producto_nombre,
    tipo_producto,
    lote_id,
    lote_codigo,
    batch,
    fecha_venc,
    SUM(cantidad_signo) AS stock_actual
FROM api.v_kardex
GROUP BY
    almacen_id,
    almacen_codigo,
    almacen_nombre,
    producto_id,
    cdg_prod,
    producto_nombre,
    tipo_producto,
    lote_id,
    lote_codigo,
    batch,
    fecha_venc
HAVING SUM(cantidad_signo) <> 0;


