-- 14. FUNCTIONS FOR AUTOMATIC RECEIVING LOGIC
-- =========================================================

CREATE OR REPLACE FUNCTION api.recalcular_estado_oc(p_orden_id integer)
RETURNS void
LANGUAGE plpgsql
AS $$
DECLARE
    v_total_lines integer;
    v_closed_lines integer;
    v_received_lines integer;
BEGIN
    SELECT
        COUNT(*),
        COUNT(*) FILTER (WHERE estado = 'CERRADA'),
        COUNT(*) FILTER (WHERE cantidad_recibida > 0)
    INTO
        v_total_lines,
        v_closed_lines,
        v_received_lines
    FROM api.ordenes_compra_detalle
    WHERE orden_id = p_orden_id;

    UPDATE api.ordenes_compra
    SET estado =
        CASE
            WHEN estado = 'ANULADA' THEN 'ANULADA'
            WHEN v_total_lines > 0 AND v_closed_lines = v_total_lines THEN 'CERRADA'
            WHEN v_received_lines > 0 THEN 'PARCIAL'
            ELSE 'ABIERTA'
        END,
        updated_at = now()
    WHERE id = p_orden_id;
END;
$$;


CREATE OR REPLACE FUNCTION api.trg_recepcion_detalle_before()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    v_lote_id integer;
BEGIN
    -- If lote_id is missing but lote_codigo exists, create or reuse lote automatically.
    IF NEW.lote_id IS NULL AND NEW.lote_codigo IS NOT NULL AND trim(NEW.lote_codigo) <> '' THEN

        INSERT INTO api.lotes (
            producto_id,
            lote_codigo,
            batch,
            fecha_venc
        )
        VALUES (
            NEW.producto_id,
            trim(NEW.lote_codigo),
            NEW.batch,
            NEW.fecha_venc
        )
        ON CONFLICT (producto_id, lote_codigo)
        DO UPDATE SET
            batch = COALESCE(EXCLUDED.batch, api.lotes.batch),
            fecha_venc = COALESCE(EXCLUDED.fecha_venc, api.lotes.fecha_venc),
            updated_at = now()
        RETURNING id INTO v_lote_id;

        NEW.lote_id := v_lote_id;
    END IF;

    RETURN NEW;
END;
$$;


CREATE OR REPLACE FUNCTION api.trg_recepcion_detalle_after()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
    v_orden_id integer;
    v_recepcion_num text;
    v_operador_id integer;
BEGIN
    SELECT
        r.orden_id,
        r.num_recepcion,
        r.operador_id
    INTO
        v_orden_id,
        v_recepcion_num,
        v_operador_id
    FROM api.recepciones r
    WHERE r.id = NEW.recepcion_id;

    -- Create automatic stock movement.
    INSERT INTO api.movimientos_stock (
        fecha_movimiento,
        movimiento_tipo,
        almacen_destino_id,
        producto_id,
        lote_id,
        cantidad,
        doc_tipo,
        doc_id,
        doc_linea_id,
        referencia,
        operador_id,
        observacion
    )
    VALUES (
        now(),
        'RECEPCION',
        NEW.almacen_id,
        NEW.producto_id,
        NEW.lote_id,
        NEW.cantidad_recibida,
        'RECEPCION',
        NEW.recepcion_id,
        NEW.id,
        v_recepcion_num,
        v_operador_id,
        NEW.observacion
    );

    -- Recalculate received quantity in the PO line.
    UPDATE api.ordenes_compra_detalle d
    SET
        cantidad_recibida = COALESCE((
            SELECT SUM(rd.cantidad_recibida)
            FROM api.recepcion_detalle rd
            WHERE rd.oc_detalle_id = d.id
        ), 0),
        estado =
            CASE
                WHEN COALESCE((
                    SELECT SUM(rd.cantidad_recibida)
                    FROM api.recepcion_detalle rd
                    WHERE rd.oc_detalle_id = d.id
                ), 0) >= d.cantidad_pedida THEN 'CERRADA'

                WHEN COALESCE((
                    SELECT SUM(rd.cantidad_recibida)
                    FROM api.recepcion_detalle rd
                    WHERE rd.oc_detalle_id = d.id
                ), 0) > 0 THEN 'PARCIAL'

                ELSE 'PENDIENTE'
            END,
        updated_at = now()
    WHERE d.id = NEW.oc_detalle_id;

    PERFORM api.recalcular_estado_oc(v_orden_id);

    RETURN NEW;
END;
$$;


CREATE TRIGGER recepcion_detalle_before_insert
BEFORE INSERT ON api.recepcion_detalle
